package ej1;

/**
 * TA05 - Que variables es mayor, menor o igual.
 * @author marcp
 *
 */
public class Ej1App {

	public static void main(String[] args) {
		int a=10;
		int b=10;
		
		if(a>b) {
			System.out.println("Variable 'a' es mayor que 'b'");
		}else if (a == b){
			System.out.println("Variable 'a' es igual que 'b'");
		}else {
			System.out.println("Variable 'b' es mayor que 'a'");
		}

	}

}
